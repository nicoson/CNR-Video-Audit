package cn.qiniu.form.manager;

public class Video {
	
//视频资源归档地址
	private String uri;
//视频资源封面地址
	private String cover;

	public String getUri() {
		return uri;
	}

	public void setUri(String uri) {
		this.uri = uri;
	}

	public String getCover() {
		return cover;
	}

	public void setCover(String cover) {
		this.cover = cover;
	}

}
